"use client";

import * as Sentry from "@sentry/nextjs";
import { useEffect } from "react";

export default function GlobalError({
  error,
  reset,
}: {
  error: Error;
  reset: () => void;
}) {
  useEffect(() => {
    Sentry.captureException(error);
  }, [error]);

  return (
    <html>
      <body>
        <h2>Algo sali√≥ mal.</h2>
        <button onClick={() => reset()}>Intentar de nuevo</button>
      </body>
    </html>
  );
}
